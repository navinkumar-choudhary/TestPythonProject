# Area of rectangle
length = int(input("Enter the Length: "))
breadth = int(input("Enter the Breadth: "))
area = length*breadth
print("Area of rectangle is: "+ str(area))

# Area of Circle
radius = float(input("Please Enter the Radius of Circle: "))
pi = 3.14159
area = pi*(radius**2)
print("Area of the Circle is: "+str(area))
